const FEEDBACK_RECENCY_HALF_LIFE_DAYS = 7

function clamp(value: number, min: number, max: number): number {
  return Math.max(min, Math.min(max, value))
}

function normalizeTrustScore(context: Record<string, unknown> | null | undefined): number {
  const raw = context?.userTrustScore ?? context?.trustScore ?? 1
  return clamp(Number(raw) || 1, 0.25, 3)
}

function recencyScoreFromIso(createdAt: string, now = Date.now()): number {
  const created = new Date(createdAt).getTime()
  if (!Number.isFinite(created)) return 1
  const ageDays = Math.max(0, (now - created) / (1000 * 60 * 60 * 24))
  const decay = Math.exp((-Math.log(2) * ageDays) / FEEDBACK_RECENCY_HALF_LIFE_DAYS)
  return clamp(Number(decay.toFixed(4)), 0.15, 1)
}

function frequencyScoreFromCount(count: number): number {
  if (count <= 1) return 1
  return clamp(Number((1 + Math.log2(count) * 0.35).toFixed(4)), 1, 2.5)
}

function buildWeightedFeedbackSignal(input: {
  weight: number
  trustScore: number
  frequencyCount: number
  actorFrequencyCount?: number
  createdAt: string
}): {
  trustScore: number
  frequencyScore: number
  recencyScore: number
  antiManipulationFactor: number
  weightedScore: number
} {
  const trustScore = clamp(Number(input.trustScore || 1), 0.25, 3)
  const frequencyScore = frequencyScoreFromCount(input.frequencyCount)
  const recencyScore = recencyScoreFromIso(input.createdAt)
  const actorFrequency = Math.max(1, Number(input.actorFrequencyCount || 1))
  const antiManipulationFactor =
    actorFrequency > 5
      ? clamp(Number(Math.sqrt(5 / actorFrequency).toFixed(4)), 0.2, 1)
      : 1
  const weightedScore = Number(
    (
      Number(input.weight || 1) *
      trustScore *
      frequencyScore *
      recencyScore *
      antiManipulationFactor
    ).toFixed(4)
  )

  return {
    trustScore,
    frequencyScore,
    recencyScore,
    antiManipulationFactor,
    weightedScore,
  }
}

function feedbackActorKeyFromContext(
  context: Record<string, unknown> | null | undefined
): string {
  const ip = String(context?.ip || '').trim().toLowerCase()
  const device = String(context?.deviceId || context?.ua || '').trim().toLowerCase()
  if (!ip && !device) return 'anon'
  return `${ip || 'noip'}|${device || 'nodevice'}`
}

